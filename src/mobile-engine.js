export function toPascal(str) {
  if (!str) return '';
  return str.replace(/(?:^|[\s_-])(\w)/g, (_, c) => c.toUpperCase());
}

export function toSnake(str) {
  if (!str) return '';
  return str.replace(/\s+/g, '_').toLowerCase().replace(/[^a-z0-9_]/g, '');
}

